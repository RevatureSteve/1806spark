package com.ex.operators;

/*
 * RELATIONAL OPERATORS
 * 
 * ==	equal to
 * != 	not equal to
 * > 	greater than
 * < 	less than 
 * >= 	greater than or equal to
 * <= 	less than or equal to
 */
public class RelationalOp {
	public static void main(String[] args) {
		
		System.out.println("5 == 7:\t" + (5 == 7));
		System.out.println("5 != 7:\t" + (5 != 7));
		System.out.println("5 >  7:\t" + (5 > 7));
		System.out.println("5 <  7:\t" + (5 < 7));
		System.out.println("5 >= 7:\t" + (5 >= 7));
		System.out.println("5 <= 7:\t" + (5 <= 7));
		
		
		
	}
}
