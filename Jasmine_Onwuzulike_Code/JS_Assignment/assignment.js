// 3. Reverse String
// Define function: reverseStr(someStr)
// Reverse and return the String.
function reverseStr(someStr) {
     var firstStr = " ";
     for (var i = someStr.length - 1 ; i >= 0; i--){
        firstStr += someStr[i];
     }
     return firstStr;
}